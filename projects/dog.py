import turtle as t

t.speed(2)

t.shape("turtle")

def dog():
	t.circle(100)
	t.penup()
	t.left(90)
	t.forward(140)
	t.right(90)
	t.forward(30)
	t.pendown()
	t.circle(5)
	t.penup()
	t.right(180)
	t.forward(60)
	t.right(180)
	t.pendown()
	t.circle(5)
	t.penup()
	t.forward(30)
	t.right(90)
	t.forward(50)
	t.right(90)
	t.pendown()
	t.circle(20)
	t.penup()
	t.left(90)
	t.forward(42)
	t.right(120)
	t.pendown()
	t.forward(45)
	t.right(180)
	t.forward(45)
	t.left(60)
	t.forward(45)
	t.right(180)
	t.forward(45)
	t.left(40)

dog()

t.exitonclick()
